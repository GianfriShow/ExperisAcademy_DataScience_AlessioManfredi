# range() returns a sequence of integers
# the arguments are: starting value (0 if omitted), stopping value (EXCLUDED from the final output sequence), step (1 by default, distance between each element)
for i in range(5):
    print(i)

for i in range(2,8):
    print(i)

for i in range(1,10,2):
    print(i)

for i in range(1,-10,-1):
    print(i)